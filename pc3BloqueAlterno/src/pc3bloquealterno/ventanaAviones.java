    private void btnAgregarAvionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarAvionActionPerformed
        // TODO add your handling code here:
        int     ID                  = arregloAviones.correlativo();
        int     capacidadMax        = Integer.parseInt(txtCapMax.getText());
        int     pasajerosActuales   = Integer.parseInt(txtPasActual.getText());
        String  estado              = lstEstado.getSelectedItem().toString();
        int     pesoMaximo          = Integer.parseInt(txtPesMax.getText());
        
        // crear objeto avion 
        // comprobando cual es el estado
        if( estado.equals("deshabilitado"))
        {
            Avion objAvion = new Avion(ID, capacidadMax, pesoMaximo, estado); 
            arregloAviones.agregarAvion(objAvion);
            arregloAviones.grabarEnArchivo(objAvion);
        }
        else if( estado.equals("habilitado"))
        {
            Avion objAvion = new Avion(ID, capacidadMax, pasajerosActuales, pesoMaximo, estado);
            arregloAviones.agregarAvion(objAvion);
            arregloAviones.grabarEnArchivo(objAvion);
        }
    }//GEN-LAST:event_btnAgregarAvionActionPerformed

    public void agregarItemComboBoxID()
    {
        ArrayList<Integer> listIDs = new ArrayList<>();
        for(int i = 0; i < arregloAviones.rows(); i++)
        {
            listIDs.add(arregloAviones.get(i).getID());
        }
        
        cmbBoxID.removeAllItems();
        cmbBoxEditar.removeAllItems();
        cmbBoxID.setSelectedItem(null);
        cmbBoxEditar.setSelectedItem(null);
        
        for (int i =0; i <listIDs.size(); i++)
        {
            cmbBoxID.addItem(listIDs.get(i).toString());
            cmbBoxEditar.addItem(listIDs.get(i).toString());
        }
    }
    
