    private void btnModifyCantMaxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifyCantMaxActionPerformed
        // TODO add your handling code here:
        try
        {
            int idSeleccioando = Integer.parseInt(cmbBoxEditar.getSelectedItem().toString());
            int capacidadMax   = Integer.parseInt(txtModifyCanMax.getText());
            
            Avion objModificar = arregloAviones.buscarcodigo(idSeleccioando);

            boolean estadoModify = arregloAviones.modificarCapacidadMaxima(objModificar, capacidadMax);
            
            if (estadoModify == true)
            {
                int nuevoPorcentaje = (objModificar.getPasajerosActuales()) / objModificar.getCapacidadMaxima();
                objModificar.setPorcentajeOcupabilidad(nuevoPorcentaje);
                arregloAviones.grabarModificareliminar();
            }
            
            JOptionPane.showMessageDialog(null,"Se modifico con exito");
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(null,"Ocurrio una excpecion: " + e);
        }
    }//GEN-LAST:event_btnModifyCantMaxActionPerformed

    public void mostrarTodosDatosTabla()
    {
        modeloTablaDatosCompletos.setRowCount(0);
        // creacion de arreglo para guardar los datos y subirlos a la tabla
        String datosMostrar[] = new String[6];
        // datos
        String partesLocal[] = null;
        try
        {
            String lineaLocal;
            BufferedReader lector = new BufferedReader(new FileReader("ListaAviones.csv")); // el lector ya tiene el archivo
            // imprimiendo el archivo
            while( (lineaLocal = lector.readLine()) != null) 
            // obten la primera linea del archivo, y si la linea no hay nada,entonces termina
            {
                partesLocal = lineaLocal.split(","); // hasta que encuentra coma cambia de parte
                for (int i=0; i < partesLocal.length; i++)
                {
                    datosMostrar[0] = partesLocal[0]; // nombre
                    datosMostrar[1] = partesLocal[1]; // capacidadMaxima
                    datosMostrar[2] = partesLocal[2]; // pasajerosActuales
                    datosMostrar[3] = partesLocal[3]; // porcentajeOcupabilidad
                    datosMostrar[4] = partesLocal[4]; // estado 
                    datosMostrar[5] = partesLocal[5];// pesoMaximo
                    //System.out.println(partes[i] + "     ");
                }
                modeloTablaDatosCompletos.addRow(datosMostrar);
            }
        }
        catch(Exception e) // lanzar un mensaje en caso de que suceda un error
        {
            JOptionPane.showMessageDialog(null, "Ha ocurrido un error de tipo: " + e);
        }
    }
    
